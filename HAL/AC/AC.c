static volatile u8 AC_State;

/*====================================================   Start_FUNCTION   ====================================================*/

void AC_Init(void)
{
	/*ADC channel direction*/
	M_DIO_enuSetPinDirection(AC_PORT,AC_PIN,DIO_Direction_Output);
	/*Setting ADC Callback*/
	ADC_Init();
}


/*====================================================   END_ FUNCTION   ====================================================*/


/*====================================================   Start_FUNCTION   ====================================================*/

void AC_ON(void)
{
	M_DIO_enuSetPinValue(AC_PORT,AC_PIN,DIO_State_High);
	AC_State=ON_STATE;
}


/*====================================================   END_ FUNCTION   ====================================================*/


/*====================================================   Start_FUNCTION   ====================================================*/

void AC_OFF(void)
{
	M_DIO_enuSetPinValue(AC_PORT,AC_PIN,DIO_State_Low);
	AC_State=OFF_STATE;

}

/*====================================================   END_ FUNCTION   ====================================================*/

void AC_AdjustValue(u8 *RequiredVal,u8*ReadVal)
{
	ADC_AdjustValue(RequiredVal,ReadVal,AC_ON,AC_OFF);

}
